package baekjoon;

import java.util.*;

public class Calc {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a, b;
		a = sc.nextInt();
		b = sc.nextInt();
		System.out.println(a + b);
		
		System.out.println(a - b);
		
		System.out.println(a * b);
		
		int c, d;
		c = sc.nextInt();
		d = sc.nextInt();
		System.out.println(c / d);

	}

}

